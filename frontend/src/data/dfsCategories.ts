/** Map from snake_case API key → Title Case display label */
export function formatCategoryLabel(apiKey: string): string {
  return apiKey
    .split("_")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/** Reverse: "Title Case Label" → snake_case API key */
export function toCategoryApiKey(label: string): string {
  return label.trim().toLowerCase().replace(/\s+/g, "_");
}
